        'email',
        'password',
        'date',
    ];

    protected $hidden = [
        'password',
        'remember_token',
    ];

    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }

    // ðŸ”‘ ã“ã“ã‹ã‚‰JWTé–¢é€£
    public function getJWTIdentifier()
